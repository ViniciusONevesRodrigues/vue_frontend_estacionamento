<<template>
  <button @click="deletarUsuario">Deletar Usu치rio</button>
</template>

<script>
export default {
  name: 'DeleteBTN',
  props: {
    compID: {
      type: String,
      required: true
    }
  },
  methods: {
    async deletarUsuario() {
      try {
        console.log(this.compID)
        const response = await fetch(`http://localhost:8081/proprietario/${this.compID}`, {
          method: 'DELETE'
        });
        if (response.ok) {
          console.log(`Usu치rio ${this.compID} deletado com sucesso.`);
        } else {
          console.error(`Erro ao deletar usu치rio ${this.compID}: ${response.statusText}`);
        }
      } catch (error) {
        console.error(`Erro ao deletar usu치rio ${this.compID}: ${error.message}`);
      }
    }
  }
}
</script>
